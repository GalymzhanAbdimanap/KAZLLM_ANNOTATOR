def get_rules(archetype, lang="ru"):
    """Возвращает правила архетипа для UI: словарь с переведёнными списками."""
    rules = ARCHETYPE_RULES.get(archetype)
    if not rules:
        return None
    return {
        "ref":   rules["ref"],
        "title": rules.get(f"title_{lang}", rules.get("title_ru", "")),
        "must":  rules.get(f"must_{lang}", rules.get("must_ru", [])),
        "avoid": rules.get(f"avoid_{lang}", rules.get("avoid_ru", [])),
        "domains": rules.get("domains", []),
    }


def all_rules(lang="ru"):
    """Все правила сразу — для страницы Справки."""
    return [(arch, get_rules(arch, lang)) for arch in ARCHETYPE_RULES.keys()]
