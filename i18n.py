def t(key: str, lang: str, *args) -> str:
    """Получить перевод по ключу для заданного языка. Если ключа нет — вернуть сам ключ."""
    entry = TRANSLATIONS.get(key)
    if entry is None:
        return key
    s = entry.get(lang) or entry.get(DEFAULT_LANG) or key
    if args:
        try:
            return s.format(*args)
        except (IndexError, KeyError):
            return s
    return s
